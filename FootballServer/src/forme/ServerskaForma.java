    public void srediTabelu() {
        ArrayList<KlasaZaTabelu> listaPomoc = new ArrayList<>();
        ArrayList<Reprezentacija> listaRep = Kontroler.getInstanca().vratiListuReprezentacija();
        ArrayList<Utakmica> listaUt = Kontroler.getInstanca().vratiListuUtakmica();
        String grupa = txtGrupa.getText();
        for (Reprezentacija r : listaRep) {
            int dGolova = 0;
            int pGolova = 0;
            boolean ubaciti = false;
            for (Utakmica u : listaUt) {
                if (grupa.isEmpty() || grupa.equals(u.getGrupa())) {

                    if (r.getReprezentacijaID() == u.getDomacin().getReprezentacijaID()) {
                        ubaciti = true;
                        dGolova += u.getGolovaDomacin();
                        pGolova += u.getGolovaGost();
                    }

                    if (r.getReprezentacijaID() == u.getGost().getReprezentacijaID()) {
                        ubaciti = true;
                        pGolova += u.getGolovaDomacin();
                        dGolova += u.getGolovaGost();
                    }

                }
            }
            int golRazlika = dGolova - pGolova;
            KlasaZaTabelu k = new KlasaZaTabelu(r.getNaziv(), dGolova, pGolova, golRazlika);
            if (ubaciti) {
                listaPomoc.add(k);
            }
        }
        Collections.sort(listaPomoc, new Comparator<KlasaZaTabelu>() {
            @Override
            public int compare(KlasaZaTabelu o1, KlasaZaTabelu o2) {
                return Integer.compare(o2.getGolRazlika(), o1.getGolRazlika());
            }
        });

        ModelTabeleServera mts = new ModelTabeleServera(listaPomoc);
        tblUtakmice.setModel(mts);
    }
