    private void napuniKomboBoxove() {
        KlijentskiZahtev kz1 = new KlijentskiZahtev();
        kz1.setOperacija(Operacije.VRATI_REPREZENTACIJE);
        KomunikacijaSaServerom.getInstanca().posaljiZahtev(kz1);

        ServerskiOdgovor so = KomunikacijaSaServerom.getInstanca().primiOdgovor();
        ArrayList<Reprezentacija> listaReprezentacija = (ArrayList<Reprezentacija>) so.getOdgovor();

        cmbDomacin.removeAllItems();
        cmbGost.removeAllItems();
        for (Reprezentacija reprezentacija : listaReprezentacija) {
            cmbDomacin.addItem(reprezentacija);
            cmbGost.addItem(reprezentacija);
        }
    }

    void srediFormu(Utakmica u) {
        this.izabranaUtakmica= u;
        txtGolovaDomacin.setText(u.getGolovaDomacin() + "");
        txtGolovaGost.setText(u.getGolovaGost() + "");
        txtGrupa.setText(u.getGrupa());
        cmbDomacin.setSelectedItem(u.getDomacin());
        cmbGost.setSelectedItem(u.getGost());
    }
