    private void btnIzmeniActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIzmeniActionPerformed
        String grupa = txtGrupa.getText();
        int golovaDomacin = Integer.parseInt(txtGolovaDomacin.getText());
        int golovaGost = Integer.parseInt(txtGolovaGost.getText());
        Reprezentacija domacin = (Reprezentacija) cmbDomacin.getSelectedItem();
        Reprezentacija gost = (Reprezentacija) cmbGost.getSelectedItem();
        Utakmica u = new Utakmica(izabranaUtakmica.getUtakmicaID(), grupa, domacin, gost, golovaDomacin, golovaGost, "Izmena");

        boolean ubaceno = ((KlijentskaForma) this.getParent()).dodajIzmenjenuUtakmicu(u);
        if (ubaceno) {
            JOptionPane.showMessageDialog(this, "Uspesno ubaceno", "Success", JOptionPane.INFORMATION_MESSAGE);
            dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Neuspesno ubaceno", "ERROR", JOptionPane.ERROR_MESSAGE);
           

        }
    }//GEN-LAST:event_btnIzmeniActionPerformed

